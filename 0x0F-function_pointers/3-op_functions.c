#include "3-calc.h"

/**
 * op_add - sun of a and b
 * @a: parameter
 * @b: parameter
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract
 * @a: parameter
 * @b: parameter
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul -  product of
 * @a: parameter
 * @b: parameter
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide
 * @a: parameter
 * @b: parameter
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module
 * @a: parameter
 * @b: parameter
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}

